#Contoh 1: Penggunaan fungsi “len” pada berbagai tipe data:
print(len("Hello")) 
print(len([1, 2, 3])) 
print(len((1, 2, 3)))

#Contoh 2: Penggunaan operator “+” pada berbagai tipe data:
print(2 + 3) 
print("Hello" + " World") 
print([1, 2] + [3, 4]) 

#Contoh 3: Penggunaan fungsi “max” pada berbagai tipe data:
print(max(2, 5))
print(max([1, 2, 3])) 
print(max("Hello")) 

#Contoh 4: Penggunaan metode “sort” pada berbagai tipe data:
a = [3, 1, 2]
a.sort()
print(a)
b = ["c", "a", "b"]
b.sort()
print(b) 